def detect_emergency(text: str) -> bool:
    lower = text.lower()
    return any(kw in lower for kw in DV_KEYWORDS)


def prepend_emergency_info(response: str, gender: str = "未知") -> str:
    if gender == "男性":
        header = MALE_EMERGENCY_HEADER
    elif gender == "女性":
        header = FEMALE_EMERGENCY_HEADER
    else:
        header = DEFAULT_EMERGENCY_HEADER
    return header + response
